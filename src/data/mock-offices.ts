export interface Office {
  id: string;
  name: string;
  slug: string;
  city: string;
  state: string;
  tier: number;
  verified: boolean;
  chat_juridico_client: boolean;
  is_plus: boolean;
  is_featured: boolean;
  description: string | null;
  website_url: string | null;
  instagram_url: string | null;
  linkedin_url: string | null;
  logo_url: string | null;
  created_at: string;
  map_position: [number, number]; // [x, z] on the city grid
  revenue_range: [number, number] | null; // faixa de faturamento mensal em reais (opcional, verificado via Asaas)
  practice_areas: string[]; // IDs from practice-areas.ts
  latitude: number | null;
  longitude: number | null;
}

export interface RankedOffice extends Office {
  rank: number | null; // null = não está no ranking (não verificado)
}

/** Formatação da faixa de faturamento */
export function formatRevenueRange(range: [number, number]): string {
  const fmt = (v: number) => {
    if (v >= 1_000_000) return `R$${(v / 1_000_000).toFixed(1).replace('.0', '')}M`;
    return `R$${(v / 1_000).toFixed(0)}K`;
  };
  return `${fmt(range[0])}–${fmt(range[1])}/mês`;
}

/** Verificados: ranking por faturamento desc (usa topo da faixa). Não verificados: ordem de criação. */
export function rankOffices(offices: Office[]): RankedOffice[] {
  const verified = offices
    .filter((o) => o.verified)
    .sort((a, b) => (b.revenue_range?.[1] ?? 0) - (a.revenue_range?.[1] ?? 0));

  const unverified = offices
    .filter((o) => !o.verified)
    .sort((a, b) => new Date(a.created_at).getTime() - new Date(b.created_at).getTime());

  const ranked: RankedOffice[] = verified.map((o, i) => ({ ...o, rank: i + 1 }));
  const unranked: RankedOffice[] = unverified.map((o) => ({ ...o, rank: null }));

  return [...ranked, ...unranked];
}

